dots_to_morse = {}
for key, val in morse_to_dots.items():
    dots_to_morse[val] = key
B=A.split("  ")
C=[]
for j in B:
    for i in list(j.split()):
        for key, val in morse_to_dots.items():
            if val==i:
                C.append(key)
    C.append(" ")
    m="".join(C)
